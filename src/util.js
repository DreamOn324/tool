/**
 * 过滤空格
 * @param  {String} str 要过滤的字符串
 * @return {String}     过滤后的字符串
 */
function trim(str) {
    return str.replace(/(^\s*)|(\s*$)/g, "");
}

module.exports.trim = trim;

